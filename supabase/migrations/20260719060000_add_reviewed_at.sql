-- 「このレースもう確認したっけ」が分からなくなる、というユーザー指摘への対応(2026-07-19)。
-- レース詳細ページにチェックボックスを置き、ユーザー自身が「確認済み」を手動でマークできるようにする。
-- 診断ロジックとは無関係な閲覧状態フラグなので、他のrace_*列とは独立させる。
alter table races
  add column reviewed_at timestamptz;

comment on column races.reviewed_at is
  'ユーザーがレース詳細ページのチェックボックスで「確認済み」にマークした時刻。nullなら未確認。診断ロジックには使わない、閲覧状態の記録専用。';
